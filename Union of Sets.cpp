#include <bits/stdc++.h>

using namespace std;

int main(){
	map <int, int> sets;
	int casos, val, conjuntos;
	
	scanf("%d", &casos);
	
	while(casos--){
		scanf("%d", &conjuntos);
		
		while(conjuntos--){
			scanf("%d", &val);
			
			sets[val];
		}
	}
	
	printf("%d\n", sets.size());
	
	
	return 0;
}
